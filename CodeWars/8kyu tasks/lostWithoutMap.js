/*
  Title:
    Beginner - Lost Without a Map

  Description:
    Given an array of integers, return a new array with each value doubled.

For example:

[1, 2, 3] --> [2, 4, 6]

  Kata Link:
    https://www.codewars.com/kata/57f781872e3d8ca2a000007e/train/javascript
*/

function maps(x) {
  return x.map((elem) => elem * 2);
}
