/*
  Title:
    Checking the value from the range

  Description:
    Write an if condition to check that the age variable is in the range between 14 and 90 inclusive.

    "Inclusive" means that the value of the age variable can be equal to 14 or 90.

  Examples:

  Kata Link:
    https://learn.javascript.ru/task/check-if-in-range
*/

function ageRange(age) {
  return age >= 14 && age <= 90;  
}
